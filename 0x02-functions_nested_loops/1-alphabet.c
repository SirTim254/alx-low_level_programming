/**
 *
 *  * print_alphabet - Prints the alphabet letters in lowercase
 *
 *   *
 *
 *    * Return: Retuen 0 on successful execution
 *
 *     */



void print_alphabet(void)

{

		char alpha;



			for (alpha = 'a'; alpha <= 'z'; alpha++)

					{

								_putchar(alpha);

									}

				_putchar('\n');

}
